import os
import json
import subprocess

# 1. PURANE YOUTUBE LINKS
fixed_content = {
    "tutorials": ["ScMzIvxBSi4", "5QrR9PZW_BI"],
    "shorts": ["uCWigClzovY", "YRaIypeXVcY"],
    "pastLives": ["ScMzIvxBSi4"],
    "playlists": ["UURfvXfU4toossqCIbuXNblA"]
}

# 2. MAIN FOLDERS SCANNING (For Library)
folders = {
    "pdfs": "files/pdfs",
    "ppts": "files/ppts",
    "images": "files/images",
    "codes": "files/codes",
    "others": "files/others"
}

data = {"pdfs": [], "ppts": [], "images": [], "codes": [], "others": []}

for category, path in folders.items():
    if os.path.exists(path):
        for filename in os.listdir(path):
            if not filename.startswith('.'):
                filepath = f"{path}/{filename}"
                title = os.path.splitext(filename)[0].replace('-', ' ').title()
                data[category].append({"title": title, "link": filepath})

# Save videos.js (Library data)
js_code = f"""// AUTO-GENERATED BY ASIF TECH AUTOMATION
const myContent = {{
    tutorials: {json.dumps(fixed_content['tutorials'])},
    shorts: {json.dumps(fixed_content['shorts'])},
    pastLives: {json.dumps(fixed_content['pastLives'])},
    pdfs: {json.dumps(data['pdfs'], indent=4)},
    ppts: {json.dumps(data['ppts'], indent=4)},
    images: {json.dumps(data['images'], indent=4)},
    codes: {json.dumps(data['codes'], indent=4)},
    others: {json.dumps(data['others'], indent=4)},
    playlists: {json.dumps(fixed_content['playlists'])}
}};"""

with open('videos.js', 'w', encoding='utf-8') as f:
    f.write(js_code)

# 3. PREMIUM VAULT SYNC (For codes.js)
CODE_DIR = "files/codes"
OUTPUT_FILE = "codes.js"

def sync_codes_to_js():
    premium_scripts = []
    if os.path.exists(CODE_DIR):
        for filename in os.listdir(CODE_DIR):
            filepath = os.path.join(CODE_DIR, filename)
            if os.path.isfile(filepath):
                with open(filepath, 'r', encoding='utf-8') as f:
                    content = f.read()
                    premium_scripts.append({
                        "name": filename,
                        "content": content,
                        "ext": filename.split('.')[-1]
                    })
    
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        f.write(f"const myPremiumScripts = {json.dumps(premium_scripts)};")
    print(f"✅ Synced {len(premium_scripts)} scripts to codes.js")

# Run Vault Sync
sync_codes_to_js()

# 4. GITHUB PUSH (Automatic)
try:
    subprocess.run(["git", "add", "."], check=True)
    subprocess.run(["git", "commit", "-m", "Auto-sync: Folders and Vault Updated"], check=True)
    subprocess.run(["git", "push"], check=True)
    print("🚀 Website updated successfully on GitHub!")
except Exception as e:
    print(f"⚠️ Git Push failed (Maybe no changes?): {e}")